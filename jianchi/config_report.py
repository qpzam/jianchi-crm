# ========== 排序优先级（数字越小越靠前）==========
LOCK_PRIORITY = {
    "创投不锁": 0,    # 🌟 创投基金，受让方不锁定，不受比例限制
    "确定不锁": 1,    # 💚 非公开发行/二级市场买入/战略配售等
    "瑕疵不锁": 2,    # 💛 协议转让/司法拍卖等，需与股东确认
    "瑕疵锁定": 3,    # 🔴 瑕疵来源且持股>=5%
    "确定锁定": 4,    # 🔒 IPO前取得/股权激励
    "待确认":   5,    # ❓ 股份来源不明
}

# 排序规则：
# 第一层：LOCK_PRIORITY 锁定状态（小的在前）
# 第二层：减持比例降序（大的在前）
# 第三层：有联系方式优先（有=0，无=1）
def sort_key(record, has_contact):
    lock_status = record.get("锁定状态", "待确认")
    priority = LOCK_PRIORITY.get(lock_status, 5)
    ratio = float(record.get("减持比例(%)", 0) or 0)
    contact_flag = 0 if has_contact else 1
    return (priority, -ratio, contact_flag)

